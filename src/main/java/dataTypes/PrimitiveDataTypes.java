package dataTypes;

public class PrimitiveDataTypes {

    // WHOLE NUMBERS:

    // -128 to 127, ignore it
    byte myByte = 12;

    // -32,768 to 32,767 - never use this, really
    short myShort = 1234;

    // -2,147,483,648 to 2,147,483,648
    int myInt = 1000;

    // -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807
    long myLong = 1234567891231l;

    // DECIMAL NUMBERS

    // 6 decimal digits
    float myFloat = 6.3f;

    // 15 decimal digits
    double myDouble = 6.31232;

    // BOOLEAN
    boolean myBoolean = true;

    // CHARACTERS
    char myCHar = 'a';
}
